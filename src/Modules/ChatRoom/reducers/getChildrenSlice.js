import { createSlice } from "@reduxjs/toolkit";
import { getChildren } from "../service";

const initialState = {
  data: null,
  token: localStorage.getItem("token") || null,
  isLoading: false,
  isSuccess: !!localStorage.getItem("token"),
  isError: false,
  message: null,
};

const getChildrenSlice = createSlice({
  name: "getChildren",
  initialState,
  extraReducers: (builder) => {
    builder.addCase(getChildren.pending, (state) => {
      state.isLoading = true;
      state.isError = false;
      state.isSuccess = false;
      state.message = null;
    });
    builder
      .addCase(getChildren.fulfilled, (state, action) => {
        state.isLoading = false;
        state.isError = false;
        state.isSuccess = true;
        state.data = action.payload;
        state.message = "childrens fetched successfully";
      })
      .addCase(getChildren.rejected, (state, action) => {
        state.isLoading = false;
        state.isError = true;
        state.message = action.error.message;
      });
  },
});

export default getChildrenSlice.reducer;
